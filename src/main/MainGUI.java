    private void newDialogSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newDialogSaveButtonActionPerformed
        if (newNameValue.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Name can`t be empty!");
        } else if (newAgeValue.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Age can`t be empty!");
        } else if (newAddressValue.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Address can`t be empty!");
        } else if (newSalaryValue.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Salary can`t be empty!");
        } else {

            Session session = HibernateUtil.createSessionFactory().openSession();
            Transaction tx = null;
            Employee employee = new Employee(newNameValue.getText(), Integer.parseInt(newAgeValue.getText()), newAddressValue.getText(), Double.parseDouble(newSalaryValue.getText()));

            try {
                tx = session.beginTransaction();
                session.persist(employee);
                tx.commit();
                JOptionPane.showMessageDialog(null, "New employee saved successfully!");
                newEmployeeDialog.dispose();
                statusLabel.setText("New employee saved successfully!");

            } catch (HibernateException ex) {
                if (tx != null) {
                    tx.rollback();
                    statusLabel.setText(ex.getMessage());
                }
            } finally {
                HibernateUtil.close();
            }
        }
    }//GEN-LAST:event_newDialogSaveButtonActionPerformed

    private void editDialogButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editDialogButtonActionPerformed
        if (editIDValue.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Employee ID can`t be null!");
        } else {

            Session session = HibernateUtil.createSessionFactory().openSession();
            Transaction tx = null;

            try {

                tx = session.beginTransaction();

                Employee employee = (Employee) session.get(Employee.class, Integer.parseInt(editIDValue.getText()));

                if (!(editNameValue.getText().equals(""))) {
                    employee.setName(editNameValue.getText());
                }
                if (!(editAgeValue.getText().equals(""))) {
                    employee.setAge(Integer.parseInt(editAgeValue.getText()));
                }
                if (!(editAddressValue.getText().equals(""))) {
                    employee.setAddress(editAddressValue.getText());
                }
                if (!(editSalaryValue.getText().equals(""))) {
                    employee.setSalary(Double.parseDouble(editSalaryValue.getText()));
                }

                session.update(employee);
                tx.commit();
                JOptionPane.showMessageDialog(null, "Changes saved successfully!");
                editEmployeeDialog.dispose();
                statusLabel.setText("Changes saved successfully!");
            } catch (HibernateException e) {
                if (tx != null) {
                    tx.rollback();
                }
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                statusLabel.setText(e.getMessage());
            } finally {
                HibernateUtil.close();
            }

        }
    }//GEN-LAST:event_editDialogButtonActionPerformed

