    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();

        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;

        String hql = "from Employee";

        Query query = session.createQuery(hql);
        List<Employee> employees = null;

        try {

            tx = session.beginTransaction();

            employees = query.list();

            tx.commit();

        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            statusLabel.setText(e.getMessage());
        } finally {
            HibernateUtil.close();
        }

        for (int i = 0; i < employees.size(); i++) {
            int data1 = employees.get(i).getId();
            String data2 = employees.get(i).getName();
            int data3 = employees.get(i).getAge();
            String data4 = employees.get(i).getAddress();
            double data5 = employees.get(i).getSalary();

            Object[] row = {data1, data2, data3, data4, data5};
            model.addRow(row);
        }
        statusLabel.setText("Employees loaded");

    }//GEN-LAST:event_formWindowOpened

    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();
        model.setRowCount(0);

        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;

        String allEmployeesHQL = "from Employee";
        String olderThanHQL = "from Employee as employee where employee.age >" + olderThanValue.getText();
        String youngerThanHQL = "from Employee as employee where employee.age <" + youngerThanValue.getText();
        String salaryHigherHQL = "from Employee as emloyee where emloyee.salary >" + salaryHigherValue.getText();
        String salaryLowerHQL = "from Employee as employee where employee.salary <" + salaryLowerValue.getText();
        List<Employee> employees = null;
        Query query = null;

        if (allEmployeesButton.isSelected()) {
            query = session.createQuery(allEmployeesHQL);
            statusLabel.setText("Showing all employees");
        } else if (olderThanButton.isSelected()) {
            query = session.createQuery(olderThanHQL);
            statusLabel.setText("Showing employees older than " + olderThanValue.getText());
        } else if (youngerThanButton.isSelected()) {
            query = session.createQuery(youngerThanHQL);
            statusLabel.setText("Showing employees younger than " + youngerThanValue.getText());
        } else if (salaryHigherButton.isSelected()) {
            query = session.createQuery(salaryHigherHQL);
            statusLabel.setText("Showing employees with salary higher than " + salaryHigherValue.getText());
        } else if (salaryLowerButton.isSelected()) {
            query = session.createQuery(salaryLowerHQL);
            statusLabel.setText("Showing employees with salary lower than " + salaryLowerValue.getText());
        }

        try {

            tx = session.beginTransaction();

            employees = query.list();

            tx.commit();

        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            statusLabel.setText(e.getMessage());
        } finally {
            HibernateUtil.close();
        }
        for (int i = 0; i < employees.size(); i++) {
            int data1 = employees.get(i).getId();
            String data2 = employees.get(i).getName();
            int data3 = employees.get(i).getAge();
            String data4 = employees.get(i).getAddress();
            double data5 = employees.get(i).getSalary();

            Object[] row = {data1, data2, data3, data4, data5};
            model.addRow(row);
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

